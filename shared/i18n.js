let _lang = localStorage.getItem('springs-lang') || 'fr';

export const getLang = () => _lang;

export const t = (key, params = {}) => {
  const str = translations[_lang]?.[key] ?? translations.fr[key] ?? key;
  return str.replace(/\{(\w+)\}/g, (_, k) => params[k] !== undefined ? params[k] : `{${k}}`);
};

export const setLang = (lang) => {
  _lang = lang;
  localStorage.setItem('springs-lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = t(el.dataset.i18nTitle);
  });
  // Update toggle buttons
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.textContent = _lang === 'fr' ? 'EN' : 'FR';
    btn.title = _lang === 'fr' ? 'Switch to English' : 'Passer en français';
  });
};

export const initLang = () => {
  setLang(_lang);
};
